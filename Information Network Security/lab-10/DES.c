void permute(char in[], char out[], int table[], int n){
    for(int i=0;i<n;i++)
        out[i] = in[table[i]-1];
    out[n]='\0';
}

void XOR(char a[], char b[], char out[], int n){
    for(int i=0;i<n;i++)
        out[i] = (a[i]==b[i])?'0':'1';
    out[n]='\0';
}

void left_shift(char key[], int shifts){
    while(shifts--){
        char first = key[0];
        for(int i=0;i<27;i++)
            key[i] = key[i+1];
        key[27]=first;
    }
}

void hex_to_bin(char hex[], char bin[]){
    for(int i=0;i<16;i++){
        int val = (hex[i]<='9')?hex[i]-'0':toupper(hex[i])-'A'+10;
        for(int j=3;j>=0;j--)
            bin[i*4+(3-j)] = ((val>>j)&1)+'0';
    }
    bin[64]='\0';
}

void bin_to_hex(char bin[], char hex[]){
    for(int i=0;i<16;i++){
        int val=0;
        for(int j=0;j<4;j++)
            val = val*2 + (bin[i*4+j]-'0');
        hex[i]=(val<10)?val+'0':val-10+'A';
    }
    hex[16]='\0';
}

